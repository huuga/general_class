#include <string.h>

void *ft_memchr(void *s, int c, size_t n)
{	
	char		f;

	f = c;
	while (n > 0)
	{
		if (*(char *)s == f)
		{
			return(s);
		}
		s++;
		n--;
	}	
	return (NULL);
}